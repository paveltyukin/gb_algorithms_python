    # Если знак "0", то выхожу из цикла
    if user_sign == '0':
        break

    # Если знак оперции не найден в строке str_sign,
    # то к началу цикла
    if str_sign.find(user_sign) == -1:
        print('Повторите ввод знака операции')
        continue

    # Запрашиваю у пользователя 2 числа
    x = float(input("x="))
    y = float(input("y="))

    # Если знак операции == "/"
    # и второе число (у == 0), то вывожу предупреждение
    if user_sign == '/' and y == 0:
        print("Нельзя делить на ноль!")
    else:
        # Сохраняю в result результат строковых операций
        result = eval(f'{x}{user_sign}{y}')
        print(result)